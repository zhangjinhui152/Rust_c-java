package cn.code.ArraysAnfMath;



public class math01 {
//abs 绝对值
	//ceil 向上取整
	//floor 向下取整
	//long round 四舍五入
	
	public static void main(String[] args) {
	System.out.println(Math.abs(-12));
	System.out.println(Math.ceil(13.3));
	System.out.println(Math.floor(13.3));
	System.out.println(Math.round(12.5));
	}
}
